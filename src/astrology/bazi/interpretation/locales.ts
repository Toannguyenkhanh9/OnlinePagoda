export function normalizeInterpretationLanguage(
  language?: string,
): InterpretationLanguage {
  const value = language?.toLowerCase() ?? 'en';

  if (value.startsWith('vi')) {
    return 'vi';
  }

  if (value.startsWith('zh')) {
    return 'zh';
  }

  if (value.startsWith('ko')) {
    return 'ko';
  }

  if (value.startsWith('ja')) {
    return 'ja';
  }

  return 'en';
}

const DICTIONARIES: Record<
  InterpretationLanguage,
  Dictionary
> = {
  vi: VI,
  en: EN,
  zh: ZH,
  ko: KO,
  ja: JA,
};

export function translateInterpretationCode(
  code: string,
  language: InterpretationLanguage | string,
): string {
  const normalizedLanguage =
    normalizeInterpretationLanguage(language);

  const dictionary =
    DICTIONARIES[normalizedLanguage] ??
    EN;

  return (
    dictionary[code] ??
    EN[code] ??
    code
  );
}